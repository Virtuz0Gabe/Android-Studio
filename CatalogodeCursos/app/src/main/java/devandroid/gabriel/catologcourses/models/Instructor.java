package devandroid.gabriel.catologcourses.models;

public class Instructor {
    private String name;
    private String bio;


    // === GETTERS =================================================================
    public String getName() {
        return name;
    }
    public String getBio() {
        return bio;
    }
    // === SETTERS =================================================================
    public void setName(String name) {
        this.name = name;
    }
    public void setBio(String bio) {
        this.bio = bio;
    }
}
